function slugify(text: string) {
  return text
    .toLowerCase()
    .trim()
    .replace(/\s+/g, "-")
    .replace(/[^\w-]+/g, "");
}

function capitalize(text: string) {
  return text.charAt(0).toUpperCase() + text.slice(1);
}

export const cityCabRoutes = Object.fromEntries(
  Object.entries(cityDestinations).map(([cityKey, destinations]) => {
    const routes = destinations.map((item) => {
      const fromSlug = slugify(cityKey);
      const toSlug = slugify(item.to);

      return {
        url: `/${fromSlug}/${fromSlug}-to-${toSlug}-cab-fare`,
        from: CITY_DISPLAY_NAMES[cityKey] || capitalize(cityKey),
        to: item.to,
        distance: item.distance,
      };
    });

    return [cityKey, routes];
  }),
);
