// Price calculation function
export const calculateFare = (
  distanceKm: number,
  pricePerKm: number = 10,
): number => {
  const DRIVER_CHARGE = 500;
  const BASE_FARE = 500;

  // If distance is less than or equal to 4km, use base fare
  if (distanceKm <= 4) {
    return BASE_FARE;
  }

  // For distances > 4km, calculate based on per km rate + driver charge
  const fare = distanceKm * pricePerKm + DRIVER_CHARGE;
  return Math.round(fare);
};

// Get vehicle-specific price per km
export const getPricePerKm = (vehicle: string): number => {
  const prices: Record<string, number> = {
    dzire: 10,
    amaze: 10,
    ertiga: 13,
    innovaCrysta: 16,
    sedan: 11,
    innova: 16,
    suv: 14,
    tempo: 19,
    hatchback: 9,
    default: 10,
  };
  return prices[vehicle.toLowerCase()] || prices.default;
};

