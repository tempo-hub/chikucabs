// DYNAMIC PRICING HELPERS
// ============================================

// Get route with calculated price
export const getRouteWithPrice = (
  route: AirportRoute,
): AirportRoute & { calculatedPrice: number } => {
  const pricePerKm = getPricePerKm(route.recommended);
  const calculatedPrice = calculateFare(route.distanceKm, pricePerKm);
  return {
    ...route,
    calculatedPrice,
  };
};

// Get all routes with calculated prices
export const getRoutesWithPrices = (
  routes: AirportRoute[],
): (AirportRoute & { calculatedPrice: number })[] => {
  return routes.map((route) => getRouteWithPrice(route));
};

// Get routes by city with calculated prices
export const getRoutesByCityWithPrices = (
  city: string,
): (AirportRoute & { calculatedPrice: number })[] => {
  const routes = getRoutesByCity(city);
  return getRoutesWithPrices(routes);
};

// Get pickup routes by city with calculated prices
export const getPickupRoutesWithPrices = (
  city: string,
): (AirportRoute & { calculatedPrice: number })[] => {
  const routes = getPickupRoutes(city);
  return getRoutesWithPrices(routes);
};

// Get drop routes by city with calculated prices
export const getDropRoutesWithPrices = (
  city: string,
): (AirportRoute & { calculatedPrice: number })[] => {
  const routes = getDropRoutes(city);
  return getRoutesWithPrices(routes);
};

// Get popular routes by city with calculated prices
export const getPopularRoutesWithPrices = (
  city: string,
): (AirportRoute & { calculatedPrice: number })[] => {
  const routes = getPopularRoutesByCity(city);
  return getRoutesWithPrices(routes);
};
